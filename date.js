exports.getDate = function () {

    const today = new Date();

    const options = {
        weekday: "long",
        day: "numeric",
        month: "long"
    };

    //For getting day in Indian Hindi Language use hi-In
    return today.toLocaleDateString("en-In", options);
}

exports.getDay = function () {

    const today = new Date();

    const options = {
        weekday: "long"
    };

    return today.toLocaleDateString("en-In", options);
}

